'''Slicing'''

#Slicing
b = "Hello, World!"
print(b[2:5])
print()

#Slice From the Start
b = "Hello, World!"
print(b[:5])
print()

#Slice To the End
b = "Hello, World!"
print(b[2:])
print()


#Negative Indexing
'''Get the characters:

From: "o" in "World!" (position -5)

To, but not included: "d" in "World!" (position -2):'''

b = "Hello, World!"
print(b[-5:-2])

